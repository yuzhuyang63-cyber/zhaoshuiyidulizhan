function getBrowserLanguage() {
  const browserLanguages = Array.isArray(navigator.languages) && navigator.languages.length
    ? navigator.languages
    : [navigator.language];
  const matchedLang = browserLanguages
    .map((lang) => String(lang || "").toLowerCase().split("-")[0])
    .find((lang) => supportedLanguages.has(lang));
  return matchedLang || "en";
}

async function detectLanguageByIpCountry() {
  const controller = new AbortController();
  const timeoutId = window.setTimeout(() => controller.abort(), 1800);
  try {
    const response = await fetch("https://ipapi.co/json/", {
      signal: controller.signal,
      cache: "no-store"
    });
    if (!response.ok) {
      return "";
    }

    const payload = await response.json();
    const countryCode = String(payload.country_code || payload.country || "").toUpperCase();
    const mappedLanguage = countryLanguageMap[countryCode];
    return supportedLanguages.has(mappedLanguage) ? mappedLanguage : "";
  } catch (error) {
    return "";
  } finally {
    window.clearTimeout(timeoutId);
  }
}

async function initializeLanguage() {
  const savedLang = localStorage.getItem(languageStorageKey);
  if (supportedLanguages.has(savedLang)) {
    setLanguage(savedLang);
    return;
  }

  const browserLang = getBrowserLanguage();
  setLanguage(browserLang, { persist: false });

  if (localStorage.getItem(languageManualStorageKey) === "1") {
    return;
  }

  const geoLang = await detectLanguageByIpCountry();
  if (geoLang && !localStorage.getItem(languageStorageKey)) {
    setLanguage(geoLang, { persist: false });
  }
}

