function initHomeBanner() {
  if (document.body.dataset.page !== "home") {
    return;
  }

  const slides = Array.from(document.querySelectorAll(".home-banner-slide"));
  const prevButton = document.querySelector("#banner-prev");
  const nextButton = document.querySelector("#banner-next");
  const progressFill = document.querySelector("#banner-progress-fill");
  const banner = document.querySelector("#home-banner");

  if (slides.length <= 1) {
    return;
  }

  let activeIndex = 0;
  let timerId = null;
  const interval = 4800;

  const render = (index) => {
    activeIndex = index;
    slides.forEach((slide, slideIndex) => {
      slide.classList.toggle("is-active", slideIndex === index);
    });

    if (progressFill) {
      progressFill.style.transition = "none";
      progressFill.style.width = "0%";
      requestAnimationFrame(() => {
        requestAnimationFrame(() => {
          progressFill.style.transition = `width ${interval}ms linear`;
          progressFill.style.width = "100%";
        });
      });
    }
  };

  const start = () => {
    clearInterval(timerId);
    timerId = window.setInterval(() => {
      render((activeIndex + 1) % slides.length);
    }, interval);
  };

  const stop = () => {
    clearInterval(timerId);
  };

  prevButton?.addEventListener("click", () => {
    render((activeIndex - 1 + slides.length) % slides.length);
    start();
  });

  nextButton?.addEventListener("click", () => {
    render((activeIndex + 1) % slides.length);
    start();
  });

  banner?.addEventListener("mouseenter", stop);
  banner?.addEventListener("mouseleave", start);

  render(0);
  start();
}

