function resolveApiEndpoints(path, configuredEndpoint) {
  const configuredEndpoints = [configuredEndpoint].filter(Boolean);
  const localDevEndpoints = [`http://127.0.0.1:8000${path}`, `http://localhost:8000${path}`];
  const isFilePage = window.location.protocol === "file:";
  const isLocalHost = ["127.0.0.1", "localhost"].includes(window.location.hostname);
  const sameOriginEndpoint = isFilePage ? null : `${window.location.origin}${path}`;

  const autoEndpoints = isFilePage
    ? localDevEndpoints
    : isLocalHost
      ? [...localDevEndpoints, sameOriginEndpoint]
      : [sameOriginEndpoint];

  return [...new Set([...configuredEndpoints, ...autoEndpoints].filter(Boolean))];
}

