function localizeTechTable(tableData, lang) {
  const labels = techTableLabelTranslations[lang];
  if (!labels || !tableData?.columns?.length || !tableData?.rows?.length) {
    return tableData;
  }

  return {
    columns: tableData.columns.map((cell) => labels[cell] || cell),
    rows: tableData.rows.map((row) => row.map((cell) => labels[cell] || cell))
  };
}

function buildFallbackTechTable(product, content, lang) {
  const specs = Array.isArray(content?.specs) ? content.specs : [];
  if (!specs.length) {
    return null;
  }

  const firstColumn = lang === "zh" ? "参数" : "Item";
  const secondColumn = content?.name || (lang === "zh" ? "产品" : "Product");

  return {
    columns: [firstColumn, secondColumn],
    rows: specs.map((item) => [item.label, item.value])
  };
}

function buildFallbackIntroImages(product) {
  const images = Array.isArray(product?.gallery) ? product.gallery.slice(1) : [];
  return images.filter(Boolean).slice(0, 4);
}

function normalizeProductDetailDatasets() {
  Object.values(productCatalog).forEach((product) => {
    if (!Array.isArray(product.introImages) || !product.introImages.length) {
      product.introImages = buildFallbackIntroImages(product);
    }

    if (!product.techTable && product.en) {
      product.techTable = buildFallbackTechTable(product, product.en, "en");
    }

    if (!product.techTableZh && product.zh) {
      product.techTableZh = buildFallbackTechTable(product, product.zh, "zh");
    }
  });
}

